package myproject;

public class Swapping {

	public static void main(String[] args) {
		 int a=10;
	       int b=5;
	       System.out.println("_before swapping_");
	       System.out.println("First number is "+a);
	       System.out.println("Second number is "+b);
	       int temp=a;
	       a=b;
	       b=temp;
	       System.out.println("_ swapping_");
	       System.out.println("First number is "+a);
	       System.out.println("Second number is "+b);

	}

}
